  @override
  Future<Either<Failure, Story>> updateStory(Story story) async {
    try {
      final user = SupabaseService.client.auth.currentUser;
      if (user == null) {
        return Left(ServerFailure('User not authenticated'));
      }

      final data = {
        'id': story.id,
        'user_id': user.id,
        'title': story.title,
        'content': story.story,
        'image_url': story.imageUrl,
        'language': story.attributes.languageStyle,
        'attributes': _attributesToMap(story),
        'is_favourite': story.isFavorite,
        'updated_at': DateTime.now().toIso8601String(),
      };

      final response = await SupabaseService.client
          .from('stories')
          .upsert(data, onConflict: 'id')
          .select()
          .single();

      final updatedStory = _mapSupabaseRowToStory(response);

      // Update the local Brick/SQLite cache only (Supabase already updated above)
      // Use sqliteProvider directly to avoid any remote sync attempts
      final model = StoryModel.fromDomain(updatedStory);
      await repository.sqliteProvider.upsert<StoryModel>(
        model,
        repository: repository,
      );

      return Right(updatedStory);
    } catch (e) {
      return Left(ServerFailure('Failed to save story: ${e.toString()}'));
    }
  }

  @override
  Future<Either<Failure, Story>> likeStory(Story story, bool isLiked) async {
    try {
      final user = SupabaseService.client.auth.currentUser;
      if (user == null) {
        return Left(ServerFailure('User not authenticated'));
      }

      if (isLiked) {
        await SupabaseService.client.from('bookmarks').upsert({
          'user_id': user.id,
          'bookmarkable_type': 'story',
          'bookmarkable_id': story.id,
        }, onConflict: 'user_id,bookmarkable_type,bookmarkable_id');
      } else {
        await SupabaseService.client
            .from('bookmarks')
            .delete()
            .eq('user_id', user.id)
            .eq('bookmarkable_type', 'story')
            .eq('bookmarkable_id', story.id);
      }

      final updatedStory = story.copyWith(isFavorite: isLiked);
      await repository.sqliteProvider.upsert<StoryModel>(
        StoryModel.fromDomain(updatedStory),
        repository: repository,
      );

      return Right(updatedStory);
    } catch (e) {
      return Left(ServerFailure('Failed to save story: ${e.toString()}'));
    }
  }

  @override
  Future<Either<Failure, List<Story>>> getGeneratedStories({
    int limit = 10,
    int offset = 0,
  }) async {
    try {
      final user = SupabaseService.client.auth.currentUser;
      if (user == null) {
        return Left(ServerFailure('User not authenticated'));
      }

      final response = await SupabaseService.client
          .from('stories')
          .select()
          .eq('user_id', user.id)
          .order('updated_at', ascending: false)
          .range(offset, offset + limit - 1);

      return Right(response.map((row) => _mapSupabaseRowToStory(row)).toList());
    } catch (e) {
      return Left(ServerFailure('Failed to fetch story: ${e.toString()}'));
    }
  }

