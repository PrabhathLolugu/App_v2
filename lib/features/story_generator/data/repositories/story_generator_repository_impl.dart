  @override
  Future<Either<Failure, Story>> generateStory({
    required StoryPrompt prompt,
    required GeneratorOptions options,
  }) async {
    try {
      final result = await _remoteDataSource.generateStory(
        prompt: prompt,
        options: options,
      );

      final user = SupabaseService.client.auth.currentUser;
      final userRepo = UserRemoteDataSourceSupabase(SupabaseService.client);
      final author = await userRepo.getUserById(user!.id);
      final title = result['title'] as String? ?? 'Untitled Story';
      final storyContent = result['story'] as String? ?? '';
      final moral = result['moral'] as String? ?? '';
      final reference = result['reference'] as String? ?? '';
      final characters = (result['characters'] as List<dynamic>? ?? const [])
          .map((e) => e.toString())
          .toList();

      // Extract LLM-provided attributes (if any)
      final attributes =
          (result['attributes'] as Map?)?.cast<String, dynamic>() ??
          <String, dynamic>{};

      final scriptureFromAttributes =
          (attributes['references'] is List &&
              (attributes['references'] as List).isNotEmpty)
          ? (attributes['references'] as List).first.toString()
          : null;
      final scriptureFromResult = result['scripture'] as String?;

      // LLM-first scripture selection, then prompt, then generic fallback
      final scripture = (scriptureFromAttributes?.trim().isNotEmpty ?? false)
          ? scriptureFromAttributes!.trim()
          : (scriptureFromResult?.trim().isNotEmpty ?? false)
          ? scriptureFromResult!.trim()
          : (prompt.scripture?.trim().isNotEmpty ?? false)
          ? prompt.scripture!.trim()
          : 'Unknown';

      final triviaFromLlm = (result['trivia'] as String?)?.trim() ?? '';
      final quotesFromLlm = (result['quotes'] as String?)?.trim() ?? '';
      final activityFromLlm = (result['activity'] as String?)?.trim() ?? '';

      // Local fallback generation for UI fields when LLM does not provide them
      final theme = prompt.theme ?? 'Dharma';

      final storyMap = {
        'user_id': user.id,
        'title': title,
        'content': storyContent,
        'language': options.language.displayName,
        'metadata': {
          'scripture': scripture,
          'trivia': triviaFromLlm,
          'quotes': quotesFromLlm,
          'activity': activityFromLlm,
          'lesson': moral,
        },
        'attributes': {
          'quotes': quotesFromLlm.isNotEmpty
              ? quotesFromLlm
              : _getQuotes(theme),
          'trivia': triviaFromLlm.isNotEmpty
              ? triviaFromLlm
              : _getTrivia(scripture),
          'activity': activityFromLlm.isNotEmpty
              ? activityFromLlm
              : _getActivity(theme),
          'scripture': scripture,
          'theme': theme,
          'references':
              attributes['references'] ??
              (reference.isNotEmpty ? reference : scripture),
          'characters': attributes['characters'] ?? characters,
          'character_details': <String, dynamic>{},
          'moral': moral.isNotEmpty ? moral : _getLesson(theme),
          'story_type': prompt.storyType,
          'main_character_type': prompt.mainCharacter,
          'story_setting': prompt.setting,
          'time_era': prompt.scriptureSubtype ?? scripture,
          'narrative_perspective': 'Third Person',
          'language_style': options.language.displayName,
          'emotional_tone': 'Inspiring',
          'narrative_style': options.format.displayName,
          'plot_structure':
              '${options.length.displayName} ${options.length.description}',
          'story_length':
              '${options.length.displayName} ${options.length.description}',
          'tags': <String>[],
          'translations': {
            languageNameToCode(options.language.displayName): TranslatedStory(
              title: title,
              story: storyContent,
              moral: moral,
              trivia: triviaFromLlm.isNotEmpty
                  ? triviaFromLlm
                  : _getTrivia(scripture),
              lang: languageNameToCode(options.language.displayName),
            ),
          },
        },
        'author_id': author.id,
        'comment_count': 0,
        'share_count': 0,
        'likes': 0,
        'views': 0,
        'published_at': null,
        'image_url': null,
        'author': author.displayName,
        'is_favourite': false,
      };

      final response = await SupabaseService.client
          .from('stories')
          .insert(storyMap)
          .select()
          .single();

      final story = _mapSupabaseRowToStory(response);

      // Cache to local SQLite only (Supabase already has the record)
      // Use sqliteProvider directly to avoid any remote sync attempts
      await repository.sqliteProvider.upsert<StoryModel>(
        StoryModel.fromDomain(story),
        repository: repository,
      );

      return Right(story);
    } catch (e) {
      return Left(
        ServerFailure(
          toUserFriendlyErrorMessage(e, fallback: 'Failed to generate story'),
        ),
      );
    }
  }

