  @override
  Future<Either<Failure, Story>> getStoryById(String storyId) async {
    try {
      final user = SupabaseService.client.auth.currentUser;
      if (user == null) {
        return Left(ServerFailure('User not authenticated'));
      }

      // Fetch directly from Supabase to ensure we get the latest image_url
      final response = await SupabaseService.client
          .from('stories')
          .select()
          .eq('id', storyId)
          .maybeSingle();

      if (response == null) {
        // Fallback to local cache if not found in Supabase
        final models = await repository.get<StoryModel>(
          policy: OfflineFirstGetPolicy.localOnly,
          query: Query.where('id', storyId),
        );

        if (models.isEmpty) {
          return Left(NotFoundFailure('Story not found'));
        }

        return Right(models.first.toDomain());
      }

      final story = _mapSupabaseRowToStory(response);
      final isBookmarked = await _isStoryBookmarkedByUser(
        storyId: story.id,
        userId: user.id,
      );
      final hydratedStory = story.copyWith(isFavorite: isBookmarked);

      // Update local cache with fresh data
      await repository.sqliteProvider.upsert<StoryModel>(
        StoryModel.fromDomain(hydratedStory),
        repository: repository,
      );

      return Right(hydratedStory);
    } on OfflineFirstException catch (e) {
      return Left(CacheFailure(e.message));
    } catch (e) {
      return Left(ServerFailure('Failed to fetch story: ${e.toString()}'));
    }
  }

