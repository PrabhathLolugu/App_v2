  @override
  Future<Either<Failure, StoryChatConversation>> getOrCreateStoryChat({
    required Story story,
  }) async {
    try {
      final user = SupabaseService.client.auth.currentUser;
      if (user == null) {
        return Left(ServerFailure('User not authenticated'));
      }
      if (!_isStoryOwnedByUser(story, user.id)) {
        return Left(ServerFailure(_insightsOwnerOnlyMessage));
      }

      // Check Supabase for existing chat
      final existing = await SupabaseService.client
          .from(_storyChatTable)
          .select()
          .eq('story_id', story.id)
          .eq('user_id', user.id)
          .maybeSingle();

      if (existing != null) {
        // Found existing chat, cache and return
        final conversation = StoryChatConversation.fromSupabaseRow(existing);

        await repository.upsert<StoryChatConversationModel>(
          StoryChatConversationModel.fromDomain(conversation),
          policy: OfflineFirstUpsertPolicy.optimisticLocal,
        );

        return Right(conversation);
      }

      // Create new chat in Supabase
      final now = DateTime.now();
      final initial = StoryChatMessage(
        sender: 'bot',
        text:
            'Let\'s discuss "${story.title}". Ask me anything about the story, characters, themes, or events.',
        timestamp: now,
        storyContext: _buildStoryContext(story),
      );

      final insertBody = {
        'story_id': story.id,
        'user_id': user.id,
        'title': 'Chat about ${story.title}',
        'messages': [initial.toJson()],
      };

      final row = await SupabaseService.client
          .from(_storyChatTable)
          .insert(insertBody)
          .select()
          .single();

      final conversation = StoryChatConversation.fromSupabaseRow(row);

      // Cache to Brick
      await repository.upsert<StoryChatConversationModel>(
        StoryChatConversationModel.fromDomain(conversation),
        policy: OfflineFirstUpsertPolicy.optimisticLocal,
      );

      return Right(conversation);
    } catch (e) {
      return Left(
        ServerFailure(
          toUserFriendlyErrorMessage(e, fallback: 'Failed to load story chat'),
        ),
      );
    }
  }

  @override
  Future<Either<Failure, StoryChatConversation>> sendStoryChatMessage({
    required Story story,
    required StoryChatConversation conversation,
    required String message,
    required String language,
  }) async {
    try {
      final user = SupabaseService.client.auth.currentUser;
      if (user == null) {
        return Left(ServerFailure('User not authenticated'));
      }
      if (!_isStoryOwnedByUser(story, user.id)) {
        return Left(ServerFailure(_insightsOwnerOnlyMessage));
      }

      final now = DateTime.now();

      final updatedMessages = <StoryChatMessage>[
        ...conversation.messages,
        StoryChatMessage(sender: 'user', text: message, timestamp: now),
      ];

      final botText = await _remoteDataSource.chat(
        messages: updatedMessages.map((m) => m.toJson()).toList(),
        mode: 'story_scholar',
        storyContext: _buildStoryContext(story),
        language: language,
      );

      final withBot = <StoryChatMessage>[
        ...updatedMessages,
        StoryChatMessage(
          sender: 'bot',
          text: botText,
          timestamp: DateTime.now(),
        ),
      ];

      final updateBody = {
        'messages': withBot.map((m) => m.toJson()).toList(),
        'updated_at': DateTime.now().toIso8601String(),
      };

      final row = await SupabaseService.client
          .from(_storyChatTable)
          .update(updateBody)
          .eq('id', conversation.id)
          .select()
          .single();

      final updated = StoryChatConversation.fromSupabaseRow(row);

      // Update cache
      await repository.upsert<StoryChatConversationModel>(
        StoryChatConversationModel.fromDomain(updated),
        policy: OfflineFirstUpsertPolicy.optimisticLocal,
      );

      return Right(updated);
    } catch (e) {
      return Left(
        ServerFailure(
          toUserFriendlyErrorMessage(e, fallback: 'Failed to send message'),
        ),
      );
    }
  }

